# Арифметика с разными типами  
# - Создай переменные: a = 10, b = 3.5.  
# - Посчитай сумму, разность, произведение, деление, а также целочисленное деление и остаток от деления. Выведи результаты.  

a = 10
b = 3.5

print("\n10 + 3.5 =", a + b)
print("\n10 - 3.5 =", a - b)
print("\n10 * 3.5 =", a * b)
print("\n10 / 3.5 =", a / b)
print("\n10 % 3.5 =", a % b)